package Basicpgms;

public class Sumof10natural {

	public static void main(String[] args) {
		int n=10,s=0;
		System.out.println("first 10 number is =");
		for(int i=1;i<=n;i++)
		{
			System.out.println(i+"");
			s +=i;
			
		}
		System.out.println();
		System.out.println("sum of first 10 natural number is="+s);

	}

}
