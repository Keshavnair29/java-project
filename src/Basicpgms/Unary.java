package Basicpgms;

public class Unary {

	public static void main(String[] args) {
int a=12;
		
	
		System.out.println("a="+(a++));
		System.out.println(a);
		
		
		System.out.println("a="+(a--));
		System.out.println(a);
		
		
		System.out.println("a=" +(++a));
		System.out.println(a);
		
		
		System.out.println("a=" +(--a));
		System.out.println(a);

	}

}
